import pandas as pd


df = pd.read_csv('dados.csv')
df.isna().sum()
df.dropna()

# Preencher com m√©dia
df['coluna'] = df['coluna'].fillna(df['coluna'].mean())

# Preencher com mediana
df['coluna'] = df['coluna'].fillna(df['coluna'].median())

# Preencher com valor fixo
df.fillna(0)